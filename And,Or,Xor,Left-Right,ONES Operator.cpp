#include<iostream>
using namespace std;
int main(){
	int a=10,b=5;
	//Binary AND Operator
	cout<<"a&b is:"<<(a&b)<<endl;
	//Binary OR Operator
	cout<<"a|b is:"<<(a|b)<<endl;
	//Binary XOR Operator
	cout<<"a^b is:"<<(a^b)<<endl;
	//Left Shift Operator
	cout<<"a<<b is:"<<(a<<b)<<endl;
	//Right Shift Operator
	cout<<"a>>b is:"<<(a>>b)<<endl;
	//One's Complement Operator
	cout<<"~(a) is:"<<~(a)<<endl;
	return 0;
	
}
