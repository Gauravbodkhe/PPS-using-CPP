#include<iostream>
using namespace std;
int main(){
	int a=6,b=4;
	//Equal to Operator
	cout<<"a==b:"<<(a==b)<<endl;
	//Greater than Operator using >
	cout<<"a>b:"<<(a>b)<<endl;
	//less than operator using <
	cout<<"a<b:"<<(a<b)<<endl;
	//less than or Equal to Operator using <=
	cout<<"a<=b:"<<(a<=b)<<endl;
	//Not Equal to Opertor
	cout<<"a!=b:"<<(a!=b)<<endl;
	return 0;
}
